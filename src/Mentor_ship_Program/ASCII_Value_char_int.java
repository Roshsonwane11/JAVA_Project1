package Mentor_ship_Program;

public class ASCII_Value_char_int {

	public static void main(String[] args) {
		
		char ch = 'a'; // Change 'A' to the character whose ASCII value you want to print

        // Casting char to int will give the ASCII value
        int asciiValue =  ch;

        System.out.println("ASCII value of " + ch + " is: " + asciiValue);
	}

}
